package ch02;

public class Ex05 {
	static int i;
	static boolean b;
	
	
	public static void main(String[] args) {
		//특수문자 리터럴
		System.out.println("자바\n"); //새로운 라인
		System.out.println("문법");
		System.out.println("a\tb\tc\t");
		
		//논리문자
		boolean t = true;
		boolean t1=!t; //false
		System.out.println(t+":"+t1);
		//자바 기본형 기본값 0
		
		System.out.println(i+":"+b);
		
	}

}
