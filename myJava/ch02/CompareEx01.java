package ch02;

public class CompareEx01 {
	public static void main(String[] args) {
		//비교연산자: ==, !=, >, <, >=, <= -> true or false
		int a = 10;
		int b = 2;
		System.out.println("a==b : "+ (a==b));
		System.out.println("a!=b : "+ (a!=b));
		System.out.println("a>b : "+ (a>b));
		System.out.println("a<b : "+ (a<b));
		System.out.println("a>=b : "+ (a>=b));
		System.out.println("a<=b : "+ (a<=b));
	}
}
